func TestRenderCodex_SyncSettings_ScriptStructure(t *testing.T) {
	cfg, err := Merge([]stack.StackID{stack.Go}, nil)
	if err != nil {
		t.Fatalf("Merge: %v", err)
	}

	files, err := RenderCodex(cfg)
	if err != nil {
		t.Fatalf("RenderCodex: %v", err)
	}

	output := string(files.SyncSettings)

	checks := []string{
		"#!/usr/bin/env bash",
		"set -euo pipefail",
		"codex-config.toml",
		"$HOME/.codex",
		"config.toml",
	}
	for _, check := range checks {
		if !strings.Contains(output, check) {
			t.Errorf("SyncSettings missing structural marker %q", check)
		}
	}
}

func TestRenderCodex_SyncSettings_CopyOnFirstRunSkipIfExists(t *testing.T) {
	cfg, err := Merge([]stack.StackID{stack.Go}, nil)
	if err != nil {
		t.Fatalf("Merge: %v", err)
	}

	files, err := RenderCodex(cfg)
	if err != nil {
		t.Fatalf("RenderCodex: %v", err)
	}

	output := string(files.SyncSettings)

	// Should use copy-on-first-run/skip-if-exists, not deep merge.
	if !strings.Contains(output, `[ ! -f "$SETTINGS_FILE" ]`) {
		t.Error("SyncSettings should contain first-run conditional check")
	}
	if strings.Contains(output, "jq") {
		t.Error("SyncSettings should NOT contain jq (no deep merge for TOML)")
	}
}

// --- Static template verification ---

func TestRenderCodex_Config_IsStatic(t *testing.T) {
	cfgGo, err := Merge([]stack.StackID{stack.Go}, nil)
	if err != nil {
		t.Fatalf("Merge (Go): %v", err)
	}

	allIDs := []stack.StackID{stack.Go, stack.Node, stack.Python, stack.Rust, stack.Ruby}
	cfgAll, err := Merge(allIDs, nil)
	if err != nil {
		t.Fatalf("Merge (all): %v", err)
	}

	filesGo, err := RenderCodex(cfgGo)
	if err != nil {
		t.Fatalf("RenderCodex (Go): %v", err)
	}

	filesAll, err := RenderCodex(cfgAll)
	if err != nil {
		t.Fatalf("RenderCodex (all): %v", err)
	}

	if !bytes.Equal(filesGo.Config, filesAll.Config) {
		t.Error("Config output differs between Go-only and all-stacks configs; it should be static")
	}
}

