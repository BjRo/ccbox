// --- Isolation tests (hand-built GenerationConfig) ---

func TestRenderCodex_DirectConfig_EmptyConfig(t *testing.T) {
	cfg := GenerationConfig{}

	files, err := RenderCodex(cfg)
	if err != nil {
		t.Fatalf("RenderCodex: %v", err)
	}

	if len(files.Config) == 0 {
		t.Error("Config is empty for zero-value GenerationConfig")
	}
	if len(files.SyncSettings) == 0 {
		t.Error("SyncSettings is empty for zero-value GenerationConfig")
	}

	// Static templates should produce valid output even with empty config.
	if !strings.Contains(string(files.Config), "approval_policy") {
		t.Error("Config should contain approval_policy even with empty config")
	}
}
